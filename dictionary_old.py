def make_numba_inputs(dict_box, retards, td, tdf):
    """
    Converte le tue strutture Python in oggetti tipizzati compatibili con @njit:
      - dict_box: lista di coppie [(c1,w1),(c2,w2)] indicizzata per canale
      - retards: lista di float (ritardi)
      - td: lista di float (Time_differences)
      - tdf: lista di float (Time_differences_fine_tuning)

    Restituisce in ordine:
      typed_dict: Dict[int64, UniTuple(float64,4)]
      typed_retards: List[float64]
      typed_td: List[float64]
      typed_tdf: List[float64]
    """
    # 1) Costruisci un Dict tipizzato: chiave = codice canale, valore = (c1, w1, c2, w2)
    typed_dict = Dict.empty(
        key_type=types.int64,
        value_type=types.UniTuple(types.float64, 4)
    )
    for channel_id, (pair1, pair2) in enumerate(dict_box):
        typed_dict[channel_id] = (pair1[0], pair1[1], pair2[0], pair2[1])

    # 2) Costruisci List tipizzate
    typed_retards = List()
    typed_td = List()
    typed_tdf = List()
    for v in retards:
        typed_retards.append(float(v))
    for v in td:
        typed_td.append(float(v))
    for v in tdf:
        typed_tdf.append(float(v))

    return typed_dict, typed_retards, typed_td, typed_tdf


def calcola_differenze_assolute(dizionario):
    risultato = []
    
    for valore in dizionario:
            
        # Estrai i primi elementi di ciascuna tupla
        primo_elemento_tupla1 = valore[0][0]
        primo_elemento_tupla2 = valore[1][0]
        
        # Calcola la differenza assoluta
        differenza_assoluta = abs(primo_elemento_tupla1 - primo_elemento_tupla2)
        
        # Aggiungi al risultato
        risultato.append(differenza_assoluta)
    
    return np.array(risultato)

